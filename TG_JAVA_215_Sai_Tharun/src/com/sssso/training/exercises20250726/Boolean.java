package com.sssso.training.exercises20250726;

public class Boolean {
	 public static void main(String[] args) {
	        boolean isSunny = true;
	        boolean isRaining = false;

	        boolean andResult = isSunny && isRaining;
	        boolean orResult = isSunny || isRaining;
	        boolean notResult = !isSunny;

	        System.out.println("And: " + andResult);
	        System.out.println("or: " + orResult);
	        System.out.println("not: " + notResult);
	    }

}
